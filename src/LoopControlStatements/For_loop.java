package LoopControlStatements;

public class For_loop {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {

            System.out.println("Value of i is: " + i);
        }

        System.out.println("-----------------------------------------------------");

        for (int j = 5; j > 0; j--) {

            System.out.println("Value of j is: " + j);
        }
    }
}
